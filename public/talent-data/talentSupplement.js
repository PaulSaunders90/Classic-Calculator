const talentText = (string, ...expressions) => (rank) => {
    const result = [string[0]];
    if (rank == 0) rank = 1
    expressions.forEach((expression, i) => {
        result.push(expression[rank - 1].toString() + string[i + 1]);
    });
    return result.join("");
};